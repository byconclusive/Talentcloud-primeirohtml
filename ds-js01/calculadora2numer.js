//Forma sem frases
function contas1 (x, y){
  console.log(`${x} + ${y} = ${x + y}`)
  console.log(`${x} - ${y} = ${x - y}`)
  console.log(`${x} * ${y} = ${x * y}`)
  console.log(`${x} / ${y} = ${x / y}`)
}

contas1(10, 56)

 
//Forma com frases
function contas2(num1, num2){
  console.log("A soma dos números é: " , num1 + num2);
  console.log("A subtração entre os números é: ", num1 - num2);
  console.log("A multiplcação entre os números é: ", num1 * num2);
  console.log("O resultado da divisão é: ", num1 / num2);
}

contas2(10, 56)

